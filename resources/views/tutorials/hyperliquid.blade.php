    <p class="text-sm text-base-content/70">HyperLiquid trades with USDC. Have USDC ready before connecting the bot.</p>
</header>
<section class="space-y-2">
    <h5 class="font-semibold">Create secure credentials</h5>
    <ol class="list-decimal space-y-1 pl-6">
        <li>Navigate to <strong>More</strong> &rarr; <strong>API</strong>.</li>
        <li>Name your API and click <strong>Generate</strong> for the API Wallet address.</li>
        <li>Select <strong>Authorize API Wallet</strong> and choose a validity period.</li>
        <li>Store your Private Key securely, then click <strong>Authorize</strong>.</li>
    </ol>
    <p>Submit the Public Wallet Address and Private Key in the Settings form to finish the setup.</p>
</section>
